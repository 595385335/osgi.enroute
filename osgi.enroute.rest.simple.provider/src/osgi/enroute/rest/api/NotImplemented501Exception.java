package osgi.enroute.rest.api;

public class NotImplemented501Exception extends Exception {
    private static final long serialVersionUID = 1L;

    public NotImplemented501Exception() {
        super();
    }

    public NotImplemented501Exception( String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace ) {
        super( message, cause, enableSuppression, writableStackTrace );
    }

    public NotImplemented501Exception( String message, Throwable cause ) {
        super( message, cause );
    }

    public NotImplemented501Exception( String message ) {
        super( message );
    }

    public NotImplemented501Exception( Throwable cause ) {
        super( cause );
    }
}
